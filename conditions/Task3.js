// Problem 3: Do While Loop
// Print all numbers from 1 to n using a do while loop.
// Example:
// For n = 5, print 1 2 3 4 5.

function printNumber() {
  var num = 1;
  do {
    console.log(num);
    num++;
  } while (num <= 5);
}
printNumber();

// reverse
function printNumber() {
  var x = 10;
  do {
    console.log(x);
    x--;
  } while (x >= 1);
}
printNumber();

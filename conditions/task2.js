// Problem 2: While Loop
// Print all numbers from 1 to n using a while loop.
// Example:
// For n = 5, print 1 2 3 4 5.

function printNumber() {
  var num = 1;
  while (num <= 5) {
    console.log(num);
    num++;
  }
}

// reverse
printNumber();
function printNumber() {
  var num = 5;
  while (num >= 1) {
    console.log(num);
    num--;
  }
}
printNumber();
